function showType(){
    var showType=document.getElementById("showType");
    var showBlog=document.getElementById("showBlog");
    showType.style.display="block";
    showBlog.style.display="none";

}

function showBlog(){
    var showType=document.getElementById("showType");
    var showBlog=document.getElementById("showBlog");
    showType.style.display="none";
    showBlog.style.display="block";

}